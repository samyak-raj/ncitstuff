console.log("Math Functions Examples:");

// 1. Math.abs() - Returns the absolute value of a number
console.log("Math.abs(-5):", Math.abs(-5)); // Output: 5

// 2. Math.ceil() - Rounds a number up to the nearest integer
console.log("Math.ceil(4.2):", Math.ceil(4.2)); // Output: 5

// 3. Math.floor() - Rounds a number down to the nearest integer
console.log("Math.floor(4.8):", Math.floor(4.8)); // Output: 4

// 4. Math.round() - Rounds a number to the nearest integer
console.log("Math.round(4.5):", Math.round(4.5)); // Output: 5

// 5. Math.max() - Returns the largest of the given numbers
console.log("Math.max(1, 5, 10):", Math.max(1, 5, 10)); // Output: 10

// 6. Math.min() - Returns the smallest of the given numbers
console.log("Math.min(1, 5, 10):", Math.min(1, 5, 10)); // Output: 1

// 7. Math.sqrt() - Returns the square root of a number
console.log("Math.sqrt(16):", Math.sqrt(16)); // Output: 4

// 8. Math.pow() - Returns the base raised to the power of the exponent
console.log("Math.pow(2, 3):", Math.pow(2, 3)); // Output: 8

// 9. Math.random() - Returns a random number between 0 and 1
console.log("Math.random():", Math.random()); // Output: Random number (e.g., 0.345)

// 10. Math.trunc() - Returns the integer part of a number by removing any fractional digits
console.log("Math.trunc(4.9):", Math.trunc(4.9)); // Output: 4

// 11. Math.sin() - Returns the sine of a number (angle in radians)
console.log("Math.sin(Math.PI / 2):", Math.sin(Math.PI / 2)); // Output: 1

// 12. Math.log() - Returns the natural logarithm (base e) of a number
console.log("Math.log(10):", Math.log(10)); // Output: 2.302585092994046